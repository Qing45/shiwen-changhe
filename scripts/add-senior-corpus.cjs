// ID 生成：8 位 hex，跟现有风格一致。用 crypto.randomBytes 不必要，
// 这里用确定性 ID（基于字符串 hash）保证可复现。
function newId(seed) {
  let h = 0;
  for (let i = 0; i < seed.length; i++) {
    h = (h * 31 + seed.charCodeAt(i)) >>> 0;
  }
  return h.toString(16).padStart(8, '0').slice(0, 8);
}

